public class UsingNumber {
  public static void main(String[] argv) {
    short s = -13666;     // t计
    System.out.println("跑计 s = " + s);
    
    long  l = 2_135_482_789L;  // ノ L 夹ボ long 俱计 
    System.out.println("跑计 l = " + l);
    
    int  i = 0b1100_0110_0011_1010;  // ┏urじ  
    System.out.println("跑计 i = " + i);
  }
}
