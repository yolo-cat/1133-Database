public class IntegerValue {
  public static void main(String[] argv) {
    System.out.println("10턨 1357       = " + 1357);
  
    int i = 0b10011001 ; // int ガ쬜, 2턨 
    System.out.println("2턨  0b10011001 = " + i);
    
    long l = 0XADEF; // long ガ쬜, 16턨 
    System.out.println("16턨 0XADEF     = " + l);
    
    short s = 01357; // short ガ쬜, 8턨
    System.out.println("8턨  01357      = " + s);
  }
}
