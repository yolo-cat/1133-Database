public class MagicPrint {

  public static void main(String[] argv){
    int i = 10;
    double d = 0.334;
    String s = "字串";
    boolean b = true;

    System.out.println(i); // 可傳入int
    System.out.println(d); // 可傳入 double
    System.out.println(s); // 可傳入String
    System.out.println(b); // boolean 也可以
  }
}